var firstNumber = 10;
var lastNumber = 20;
console.log("Result:" + (firstNumber == lastNumber)); //false        //boolean   true/false
console.log("Result:" + (firstNumber === lastNumber)); //false
console.log("Result:" + (firstNumber != lastNumber)); // true
console.log("Result:" + (firstNumber > lastNumber)); //false
console.log("Result:" + (firstNumber < lastNumber)); //true
console.log("Result:" + (firstNumber <= lastNumber)); //true
console.log("Result:" + (firstNumber >= lastNumber)); //false
